    public ArrayList<Menus> menuList() {
        ArrayList<Menus> menuList = new ArrayList<>();
        try {
//            Class.forName("com.mysql.cj.jdbc.Driver");
//            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/restaurant", "root", "");
//            System.out.println("Connecteced !!");
            String queryl = menutype1;
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(queryl);
            Menus menu;
            while (rs.next()) {
                menu = new Menus(rs.getString("Date"), rs.getString("Time"), rs.getString("Name"), rs.getString("Quantity"), rs.getString("Price"), rs.getString("Placement"));
                menuList.add(menu);
            }
            String totalcrg = menutype2;
            Statement st1 = con.createStatement();
            ResultSet rs1 = st1.executeQuery(totalcrg);
            while (rs1.next()) {
                prc = rs1.getString("SUM(Price)");
                ttl.setText(prc + " TK.");
            }
            String totalcrg1 = menutype3;
            Statement st2 = con.createStatement();
            ResultSet rs2 = st1.executeQuery(totalcrg1);
            while (rs2.next()) {
                prc1 = rs2.getString("SUM(`Cost`)");
                ttl2.setText(prc1 + " TK.");

                if (prc == null) {
                    prc = 0 + "";
                }
                if (prc1 == null) {
                    prc1 = 0 + "";
                }

                profit = Integer.parseInt(prc) - Integer.parseInt(prc1);
                ttl1.setText(profit + " TK.");

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return menuList;
    }

    public void show_menu() {
        ArrayList<Menus> list = menuList();
        DefaultTableModel model = (DefaultTableModel) totalsalestable.getModel();
        Object[] row = new Object[6];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getDate();
            row[1] = list.get(i).getTime();
            row[2] = list.get(i).getName();
            row[3] = list.get(i).getQuantity();
            row[4] = list.get(i).getPrice();
            row[5] = list.get(i).getPlacement();
            model.addRow(row);
        }
    }

