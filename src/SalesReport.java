    private void jdPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_jdPropertyChange
        // TODO add your handling code here:

        String sdate = "";
        if (jd.getDate() == null) {
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd");
            LocalDateTime now = LocalDateTime.now();
            sdate = dtf.format(now);
        } else {
            DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            sdate = df.format(jd.getDate());
        }

        menutype1 = "SELECT *  FROM `totalsales` WHERE Date = \"" + sdate + "\"";
        menutype2 = "SELECT SUM(Price)  FROM `totalsales` WHERE Date = \"" + sdate + "\"";
        menutype3 = "SELECT SUM(`Cost`) FROM `cost`  WHERE Date = \"" + sdate + "\"";
        DefaultTableModel model = (DefaultTableModel) totalsalestable.getModel();
        model.setRowCount(0);
        show_menu();
    }//GEN-LAST:event_jdPropertyChange

