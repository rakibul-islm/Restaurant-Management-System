    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:

        if (nmname.getText().isEmpty() == true) {
            JOptionPane.showMessageDialog(null, "Provide the Person Name.", "Need!", 2);
        } else if (mnprice.getText().isEmpty() == true) {
            JOptionPane.showMessageDialog(null, "Provide Total Cost.", "Need!", 2);
        } else if (des.getText().isEmpty() == true) {
            JOptionPane.showMessageDialog(null, "Provide Cost Description", "Need!", 2);
        } else {
            String prname = nmname.getText().toUpperCase();
            String prcom = des.getText();
            String prgen = mnprice.getText();

            try {
//                System.out.println("Database is connecting....");
                //                Class.forName("com.mysql.jdbc.Driver");
//                Class.forName("com.mysql.cj.jdbc.Driver");
//                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/restaurant", "root", "");
//                System.out.println("Connecteced !!");
                Statement stm = con.createStatement();

                String query = "INSERT INTO `cost`(`Date`, `Name`, `Description`, `Cost`) values(DATE_FORMAT(NOW(),'%Y-%m-%d'),?,?,?)";
                PreparedStatement ps = con.prepareStatement(query);
                ps.setString(1, prname);
                ps.setString(2, prcom);
                ps.setString(3, prgen);

                ps.executeUpdate();
                DefaultTableModel model = (DefaultTableModel) tbl.getModel();
                model.setRowCount(0);
                show_menu();
                nmname.setText("");
                des.setText("");
                mnprice.setText("");

//                
//                JOptionPane.showMessageDialog(null, "Successfully Submitted ! ", "Welcome!", 1);
            } catch (HeadlessException | SQLException e) {
                System.out.println(e);
            }
        }
        //        this.dispose();
    }//GEN-LAST:event_submitActionPerformed

