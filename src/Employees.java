    private void employeetableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_employeetableMouseClicked
        // TODO add your handling code here:
        int i = employeetable.getSelectedRow();
        TableModel model = employeetable.getModel();

        mnprice.setText(model.getValueAt(i, 1).toString());
        slry.setText(model.getValueAt(i, 5).toString());
        String type1 = model.getValueAt(i, 2).toString();
        switch (type1) {
            case "Waiter":
                mntype.setSelectedIndex(0);
                break;
            case "Cleaner":
                mntype.setSelectedIndex(1);
                break;
            case "Chef":
                mntype.setSelectedIndex(2);
                break;
        }
        String type2 = model.getValueAt(i, 3).toString();
        switch (type2) {
            case "Table - 1":
                place.setSelectedIndex(0);
                break;
            case "Table - 2":
                place.setSelectedIndex(1);
                break;
            case "Table - 3":
                place.setSelectedIndex(2);
                break;
            case "Table - 4":
                place.setSelectedIndex(3);
                break;
            case "Table - 5":
                place.setSelectedIndex(4);
                break;
            case "Table - 6":
                place.setSelectedIndex(5);
                break;
            case "Table - 7":
                place.setSelectedIndex(6);
                break;
            case "Table - 8":
                place.setSelectedIndex(7);
                break;
            case "Kitchen":
                place.setSelectedIndex(8);
                break;
            case "No need":
                place.setSelectedIndex(9);
                break;
        }
    }//GEN-LAST:event_employeetableMouseClicked

